import { readdirSync, writeFileSync, mkdirSync, readFileSync } from "node:fs";
import { join, dirname } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const root = join(__dirname, "..");
const carouselDir = join(root, "public", "carousel");
const outFile = join(root, "lib", "carousel-data.ts");

function readJpegSize(buffer) {
  let offset = 2;

  while (offset < buffer.length) {
    if (buffer[offset] !== 0xff) {
      throw new Error("Invalid JPEG marker");
    }

    const marker = buffer[offset + 1];
    const length = buffer.readUInt16BE(offset + 2);

    if (
      (marker >= 0xc0 && marker <= 0xc3) ||
      (marker >= 0xc5 && marker <= 0xc7) ||
      (marker >= 0xc9 && marker <= 0xcb) ||
      (marker >= 0xcd && marker <= 0xcf)
    ) {
      return {
        width: buffer.readUInt16BE(offset + 7),
        height: buffer.readUInt16BE(offset + 5),
      };
    }

    offset += 2 + length;
  }

  throw new Error("JPEG size not found");
}

function readPngSize(buffer) {
  return {
    width: buffer.readUInt32BE(16),
    height: buffer.readUInt32BE(20),
  };
}

function readWebpSize(buffer) {
  const chunk = buffer.toString("ascii", 12, 16);

  if (chunk === "VP8 ") {
    return {
      width: buffer.readUInt16LE(26) & 0x3fff,
      height: buffer.readUInt16LE(28) & 0x3fff,
    };
  }

  if (chunk === "VP8L") {
    const bits = buffer.readUInt32LE(21);

    return {
      width: (bits & 0x3fff) + 1,
      height: ((bits >> 14) & 0x3fff) + 1,
    };
  }

  if (chunk === "VP8X") {
    return {
      width: buffer.readUIntLE(24, 3) + 1,
      height: buffer.readUIntLE(27, 3) + 1,
    };
  }

  throw new Error("Unsupported WebP format");
}

function readImageSize(file) {
  const buffer = readFileSync(join(carouselDir, file));
  const extension = file.split(".").pop()?.toLowerCase();

  if (extension === "jpg" || extension === "jpeg") {
    return readJpegSize(buffer);
  }

  if (extension === "png") {
    return readPngSize(buffer);
  }

  if (extension === "webp") {
    return readWebpSize(buffer);
  }

  throw new Error(`Unsupported carousel image type: ${file}`);
}

const files = readdirSync(carouselDir)
  .filter((f) => /\.(jpe?g|png|webp|avif)$/i.test(f))
  .sort();

const rowCount = 3;
const perRow = Math.ceil(files.length / rowCount);
const rows = Array.from({ length: rowCount }, (_, i) =>
  files.slice(i * perRow, (i + 1) * perRow).map((f) => ({
    src: `/carousel/${f}`,
    ...readImageSize(f),
  })),
);

const banner = "// AUTO-GENERATED by scripts/gen-carousel.mjs — do not edit by hand.\n// Regenerated on every `npm run dev` and `npm run build`.\n\nexport type CarouselImage = {\n  src: string;\n  width: number;\n  height: number;\n};\n\n";
const body = rows
  .map(
    (row, i) =>
      `export const carouselRow${i + 1}: CarouselImage[] = ${JSON.stringify(row, null, 2)};`,
  )
  .join("\n\n");

mkdirSync(dirname(outFile), { recursive: true });
writeFileSync(outFile, banner + body + "\n");

console.log(`✓ carousel-data.ts written (${files.length} photos across ${rowCount} rows)`);
