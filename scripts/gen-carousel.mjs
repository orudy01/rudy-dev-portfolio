import { readdirSync, writeFileSync, mkdirSync } from "node:fs";
import { join, dirname } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const root = join(__dirname, "..");
const carouselDir = join(root, "public", "carousel");
const outFile = join(root, "lib", "carousel-data.ts");

const files = readdirSync(carouselDir)
  .filter((f) => /\.(jpe?g|png|webp|avif)$/i.test(f))
  .sort();

const rowCount = 3;
const perRow = Math.ceil(files.length / rowCount);
const rows = Array.from({ length: rowCount }, (_, i) =>
  files.slice(i * perRow, (i + 1) * perRow).map((f) => `/carousel/${f}`),
);

const banner = "// AUTO-GENERATED by scripts/gen-carousel.mjs — do not edit by hand.\n// Regenerated on every `npm run dev` and `npm run build`.\n\n";
const body = rows
  .map(
    (row, i) =>
      `export const carouselRow${i + 1}: string[] = ${JSON.stringify(row, null, 2)};`,
  )
  .join("\n\n");

mkdirSync(dirname(outFile), { recursive: true });
writeFileSync(outFile, banner + body + "\n");

console.log(`✓ carousel-data.ts written (${files.length} photos across ${rowCount} rows)`);
